    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:        
        
        if(passo >= passoapasso.size()){
            jButton1.setEnabled(false);
            if(atual.isEstFinal())
                    JOptionPane.showMessageDialog(this,"A entrada foi aceita");
            else
                JOptionPane.showMessageDialog(this,"A entrada foi rejeitada");
        }
        else{
            atual = passoapasso.get(passo);             
            if(!atual.getNome().equals("inválido")){
                ((VisualizacaoPasso)jPanel1).setEstados(estados, atual);
                jPanel1.repaint();     
            }
            
            s = s.concat( String.valueOf(entrada.charAt(i)) );
            jLabel5.setText(s);
            i++;
            passo++;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

