    private void executar_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_executar_ButtonActionPerformed
        // TODO add your handling code here:
        pontos = new ArrayList<Ponto>();
        for (int i = 0; i < numPontos; i++) {                                               ///inverte o zero para o canto inferior esquerdo
            Ponto p = new Ponto((int) (20 + Math.random() * (jPanel1.getWidth()-70)), (int) (jPanel1.getHeight() - (20 + Math.random() * (jPanel1.getHeight()-70))));
            pontos.add(p);
        }
        //pontos colineares
       // pontos.add(new Ponto(jPanel1.getHeight()-50,50));
       // pontos.add(new Ponto(jPanel1.getHeight()-70,250));
       // pontos.add(new Ponto(jPanel1.getHeight()-150,150));
       // pontos.add(new Ponto(jPanel1.getHeight()-200,200));
       // pontos.add(new Ponto(jPanel1.getHeight()-250,250));

        BufferedImage imageBuffer = new BufferedImage(jPanel1.getWidth(), jPanel1.getHeight(), BufferedImage.TYPE_INT_RGB);

        Graphics2D g2Buffer = imageBuffer.createGraphics();
        g2Buffer.setColor(Color.WHITE);
        g2Buffer.fillRect(0, 0, jPanel1.getWidth(), jPanel1.getHeight());
        g2Buffer.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);

        for (Ponto p:pontos){
            p.draw(imageBuffer);
        }
        g2Buffer.dispose();

        jPanel1.getGraphics().drawImage(imageBuffer, 0, 0, null);
    }//GEN-LAST:event_executar_ButtonActionPerformed

    private void convexHull_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convexHull_ButtonActionPerformed
        // TODO add your handling code here:
        ConvexHull cH = new ConvexHull(pontos);
        cH.execute();
        Poligono convexHull = cH.getConvexHullPoints();
        Graphics2D g2 = (Graphics2D) jPanel1.getGraphics();
        No aux = convexHull.ini;
        for (int i=0;i<convexHull.size;i++){
            Ponto p1 = aux.getP();
            Ponto p2 = aux.prox.getP();// convexHull.get(i+1);
            g2.drawLine(p1.getX(), p1.getY(), p2.getX(), p2.getY());
            aux = aux.prox;
        }
    }//GEN-LAST:event_convexHull_ButtonActionPerformed

